#!/usr/bin/env bash
# !!! AGGRESSIVE FINAL DEPRECATION SWEEP (RUST_FULL_MIGRATION_PLAN.md + PHASE4_REMOVAL_PLAN.md) !!!
# disable_rust_flywheel.sh : legacy disable shim. Python flywheel orchestration deprecated (use disable_pure... for Phase 4).
# See PHASE4_REMOVAL_PLAN.md (rollback layers, Tier 1/4 sh cleanup).
#
# ============================================================
# bin/disable_rust_flywheel.sh — Symmetric clean disable for the
# Rust-powered self-improving flywheel (2026-06 "Default for Antigravity" rollout).
#
# Run: bash bin/disable_rust_flywheel.sh
# Source (for workers that want explicit off): source bin/disable_rust_flywheel.sh 2>/dev/null || true
#
# This is the recommended, documented way to turn the flywheel OFF globally
# after the 2026-06 change that made it DEFAULT (no marker needed).
#
# It:
#   - Exports DISABLE_RUST_FLYWHEEL=1 (the single strong kill-switch honored by
#     dispatcher.sh, post_process.py, workers, agy_runner, hooks, enable script, etc.)
#   - Touches AGENTFORGE_DIR/.disable_rust_flywheel (persistent file flag, survives restarts)
#   - Unsets the positive AGENTFORGE_RUST_* vars
#   - Regenerates bin/rust_flywheel.env with the safe default-OFF logic
#   - Calls the Python activator in disable mode when possible (for patch hygiene)
#   - Is fully idempotent and safe to source
#
# Re-enable later:
#   bash bin/enable_rust_flywheel.sh --enable
#   # or simply: rm -f /home/eveselove/agentforge/.disable_rust_flywheel ; unset DISABLE_RUST_FLYWHEEL
#
# Full story: ANTIGRAVITY_DEFAULT.md (includes "What this means for Antigravity tasks")
# Ops reference: ENABLE_RUST_FLYWHEEL.md
# ============================================================

set -u

ROOT="/home/eveselove"
AGENTFORGE_DIR="$ROOT/agentforge"
SNIPPET="$AGENTFORGE_DIR/bin/rust_flywheel.env"
DISABLE_FILE="$AGENTFORGE_DIR/.disable_rust_flywheel"

_is_sourced() {
  [[ "${BASH_SOURCE[0]}" != "${0}" ]] || return 1
}

if ! _is_sourced; then
  set -euo pipefail
fi

echo "[disable_rust_flywheel] 2026-06 DEFAULT MODE: Turning Rust flywheel OFF for Antigravity + farm"

# Strong persistent + env signal (checked first in enable script, dispatcher, post_process, workers, etc.)
touch "$DISABLE_FILE" 2>/dev/null || true
export DISABLE_RUST_FLYWHEEL=1
unset AGENTFORGE_RUST_FLYWHEEL AGENTFORGE_USE_RUST AGENTFORGE_RUST_RUNNER 2>/dev/null || true

# Regenerate the snippet with explicit OFF logic (workers that source it early will stay clean)
cat > "$SNIPPET" << 'SNIP_EOF'
# AgentForge Rust Flywheel env snippet (auto-generated by disable_rust_flywheel.sh)
# Source early in grok_worker.sh / jules_worker.sh / dispatcher.sh / runners:
#   source /home/eveselove/agentforge/bin/rust_flywheel.env 2>/dev/null || true
#
# 2026-06: Rust Flywheel is DEFAULT ON for Antigravity unless explicitly disabled here.
_DISABLE_FILE="/home/eveselove/agentforge/.disable_rust_flywheel"
if [[ "${DISABLE_RUST_FLYWHEEL:-0}" = "1" ]] || [[ -f "$_DISABLE_FILE" ]]; then
    export AGENTFORGE_RUST_FLYWHEEL=0
    export AGENTFORGE_USE_RUST=0
    # Positive vars explicitly cleared for this process tree
else
    export AGENTFORGE_RUST_FLYWHEEL=1
    export AGENTFORGE_USE_RUST=1
    if [[ -x "/home/eveselove/agentforge/rust/target/release/agentforge-runner" ]]; then
      export AGENTFORGE_RUST_RUNNER="/home/eveselove/agentforge/rust/target/release/agentforge-runner"
    else
      export AGENTFORGE_RUST_RUNNER="/home/eveselove/agentforge/rust/target/debug/agentforge-runner"
    fi
fi
SNIP_EOF
chmod 644 "$SNIPPET" 2>/dev/null || true

# Also invoke Python side (best-effort, for any monkey-patches or future hygiene)
if command -v python3 >/dev/null 2>&1; then
  PYTHONPATH="$ROOT" python3 -c '
import sys, os
sys.path.insert(0, "/home/eveselove")
os.environ["DISABLE_RUST_FLYWHEEL"] = "1"
try:
    import agentforge.enable_rust_flywheel as erf
    # The module does not have a dedicated deactivate yet, but we can force the envs off
    os.environ["AGENTFORGE_RUST_FLYWHEEL"] = "0"
    os.environ["AGENTFORGE_USE_RUST"] = "0"
    print("[disable_rust_flywheel] Python envs forced off (patch hygiene)")
except Exception as _e:
    print("[disable_rust_flywheel] Python note (non-fatal):", _e, file=sys.stderr)
' 2>/dev/null || true
fi

if ! _is_sourced; then
  echo ""
  echo "✅ Rust flywheel DISABLED (Antigravity default rolled back safely)"
  echo "   - DISABLE_RUST_FLYWHEEL=1 (env)"
  echo "   - $DISABLE_FILE (persistent marker)"
  echo "   - Snippet updated: $SNIPPET"
  echo ""
  echo "All paths (post_process, dispatcher, workers, hooks, timer) will now skip Rust flywheel."
  echo "Graceful pure-Python fallback is active everywhere."
  echo ""
  echo "To re-enable:"
  echo "  bash bin/enable_rust_flywheel.sh --enable"
  echo "  # or: rm -f $DISABLE_FILE && bash bin/enable_rust_flywheel.sh"
  echo ""
  echo "See ANTIGRAVITY_DEFAULT.md for the full 'now default' story and communication package."
fi

# Always succeed (disable is a safe no-op state)
exit 0
# === PURE RUST FLYWHEEL DEFAULT (injected by make_pure_rust_flywheel_default.sh @ 2026-05-31T10:42:02+03:00) ===
# Pure Rust cutover (production excellence): when .pure_rust_flywheel or AGENTFORGE_PURE_RUST_FLYWHEEL=1 or FLYWHEEL_ENGINE=rust,
# force sole use of agentforge-runner binary for ALL flywheel/candidate/continuous orchestration.
# Complements env snippet + unit patches. Idempotent + guarded. Ultimate killswitch: DISABLE_RUST_FLYWHEEL=1.
PURE_MARKER="/home/eveselove/agentforge/.pure_rust_flywheel"
if [[ -f "$PURE_MARKER" ]] || [[ "${AGENTFORGE_PURE_RUST_FLYWHEEL:-0}" = "1" ]] || [[ "${AGENTFORGE_FLYWHEEL_ENGINE:-}" = "rust" ]]; then
    export AGENTFORGE_PURE_RUST_FLYWHEEL=1
    export AGENTFORGE_FLYWHEEL_ENGINE=rust
    if [ -x "/home/eveselove/agentforge/rust/target/release/agentforge-runner" ]; then
        export AGENTFORGE_RUST_RUNNER="/home/eveselove/agentforge/rust/target/release/agentforge-runner"
    fi
    export AGENTFORGE_FLYWHEEL_PROVENANCE="rust-agentforge-runner"
    # shellcheck disable=SC1091
    [ -f "/home/eveselove/agentforge/bin/rust_flywheel.env" ] && source "/home/eveselove/agentforge/bin/rust_flywheel.env" 2>/dev/null || true
fi
# End pure section — DISABLE_RUST_FLYWHEEL remains ultimate global off-switch everywhere.
