#!/usr/bin/env bash
# !!! AGGRESSIVE FINAL DEPRECATION SWEEP (RUST_FULL_MIGRATION_PLAN.md + PHASE4_REMOVAL_PLAN.md) !!!
# enable_rust_flywheel.sh : transitional enabler (now superseded by make_pure... + direct env in services).
# Python flywheel orchestration deprecated. This shim is low-risk Tier 1 removal candidate post-cutover.
# See PHASE4_REMOVAL_PLAN.md (Tier 1 shims, risks low, removal after all services/workers on pure binary).
# Guard/central logic in learning/utils.py. Prefer direct agentforge-runner + .pure_rust_flywheel marker.
#
# One-command enabler + sourceable injector for the full Rust-powered flywheel in the live AgentForge farm.
# Run: bash bin/enable_rust_flywheel.sh
# Source (for workers): source bin/enable_rust_flywheel.sh 2>/dev/null || true
# Idempotent, safe for sourcing, produces bin/rust_flywheel.env snippet for easy injection.
#
# 2026-06 NOTE: Rust Flywheel is now DEFAULT for Antigravity (and farm) — see ANTIGRAVITY_DEFAULT.md.
# This script is the explicit "force on + patch + snippet" tool (still essential for workers & tests).
# Primary off-switch (everywhere): DISABLE_RUST_FLYWHEEL=1 or the .disable file.
# New dedicated disable helper: bin/disable_rust_flywheel.sh (symmetric, recommended for clean rollback).
# Full communication package (blurb, benefits, continuous behavior): ANTIGRAVITY_DEFAULT.md + ENABLE_RUST_FLYWHEEL.md.

# Source-safe: avoid set -e in sourced context; use function for main logic
_is_sourced() {
  [[ "${BASH_SOURCE[0]}" != "${0}" ]] || return 1
}

if ! _is_sourced; then
  set -euo pipefail
fi

ROOT="/home/eveselove"
AGENTFORGE_DIR="$ROOT/agentforge"
RUST_BIN_RELEASE="$AGENTFORGE_DIR/rust/target/release/agentforge-runner"
RUST_BIN_DEBUG="$AGENTFORGE_DIR/rust/target/debug/agentforge-runner"
SNIPPET="$AGENTFORGE_DIR/bin/rust_flywheel.env"
DISABLE_FILE="$AGENTFORGE_DIR/.disable_rust_flywheel"

# Production polish: prefer release binary if it exists (smaller/faster), else debug
if [[ -x "$RUST_BIN_RELEASE" ]]; then
  RUST_BIN_DEFAULT="$RUST_BIN_RELEASE"
elif [[ -x "$RUST_BIN_DEBUG" ]]; then
  RUST_BIN_DEFAULT="$RUST_BIN_DEBUG"
else
  RUST_BIN_DEFAULT="$RUST_BIN_DEBUG"  # will warn later
fi

# === SAFE DEFAULT: Rust flywheel ON unless explicit disable ===
# Rollback: DISABLE_RUST_FLYWHEEL=1  or  touch .disable_rust_flywheel (in AGENTFORGE_DIR)
# Strong, simple, checked early. File flag survives restarts; env is per-process.
_is_rust_disabled() {
  [[ "${DISABLE_RUST_FLYWHEEL:-0}" = "1" ]] || [[ -f "$DISABLE_FILE" ]]
}

# Handle CLI disable/enable modes for the helper (non-sourced runs)
if ! _is_sourced; then
  case "${1:-}" in
    --disable)
      touch "$DISABLE_FILE"
      echo "[enable_rust_flywheel] --disable: created $DISABLE_FILE (strong rollback flag)"
      export DISABLE_RUST_FLYWHEEL=1
      ;;
    --enable)
      rm -f "$DISABLE_FILE"
      echo "[enable_rust_flywheel] --enable: removed $DISABLE_FILE (re-arming default-on)"
      unset DISABLE_RUST_FLYWHEEL
      ;;
  esac
fi

if _is_rust_disabled; then
    export AGENTFORGE_RUST_FLYWHEEL=0
    export AGENTFORGE_USE_RUST=0
    if ! _is_sourced; then
      echo "[enable_rust_flywheel] DISABLED (DISABLE_RUST_FLYWHEEL=1 or $DISABLE_FILE present) — Rust flywheel OFF"
    fi
else
    : "${AGENTFORGE_RUST_FLYWHEEL:=1}"
    : "${AGENTFORGE_USE_RUST:=1}"
    : "${AGENTFORGE_RUST_RUNNER:=$RUST_BIN_DEFAULT}"

    export AGENTFORGE_RUST_FLYWHEEL=1
    export AGENTFORGE_USE_RUST=1
    export AGENTFORGE_PURE_RUST_FLYWHEEL=1
    export AGENTFORGE_FLYWHEEL_ENGINE=rust
    export AGENTFORGE_RUST_RUNNER="${AGENTFORGE_RUST_RUNNER:-$RUST_BIN_DEFAULT}"
    export FLYWHEEL_PROVENANCE=rust-agentforge-runner
    export AGENTFORGE_FLYWHEEL_PROVENANCE=rust-agentforge-runner
    touch "$AGENTFORGE_DIR/.pure_rust_flywheel" 2>/dev/null || true
fi

# Generate always-sourceable snippet for worker startup injection
# Snippet implements identical safe default-on + disable (env or file) check.
cat > "$SNIPPET" << 'SNIP_EOF'
# AgentForge Rust Flywheel env snippet (auto-generated by enable_rust_flywheel.sh)
# Source early in grok_worker.sh / jules_worker.sh / dispatcher.sh / runners:
#   source /home/eveselove/agentforge/bin/rust_flywheel.env 2>/dev/null || true
# SAFE DEFAULT ON for Antigravity (Rust flywheel). Rollback: DISABLE_RUST_FLYWHEEL=1 or .disable_rust_flywheel
_DISABLE_FILE="/home/eveselove/agentforge/.disable_rust_flywheel"
if [[ "${DISABLE_RUST_FLYWHEEL:-0}" = "1" ]] || [[ -f "$_DISABLE_FILE" ]]; then
    export AGENTFORGE_RUST_FLYWHEEL=0
    export AGENTFORGE_USE_RUST=0
else
    export AGENTFORGE_RUST_FLYWHEEL=1
    export AGENTFORGE_USE_RUST=1
    export AGENTFORGE_PURE_RUST_FLYWHEEL=1
    export AGENTFORGE_FLYWHEEL_ENGINE=rust
    # Prefers release binary everywhere (prod default)
    if [[ -x "/home/eveselove/agentforge/rust/target/release/agentforge-runner" ]]; then
      _DEF_RUNNER="/home/eveselove/agentforge/rust/target/release/agentforge-runner"
    else
      _DEF_RUNNER="/home/eveselove/agentforge/rust/target/debug/agentforge-runner"
    fi
    export AGENTFORGE_RUST_RUNNER="${AGENTFORGE_RUST_RUNNER:-$_DEF_RUNNER}"
    # Rate-limit preserved (every N post_process calls)
    export AGENTFORGE_RUST_FLYWHEEL_EVERY_N="${AGENTFORGE_RUST_FLYWHEEL_EVERY_N:-5}"
    export FLYWHEEL_PROVENANCE=rust-agentforge-runner
    export AGENTFORGE_FLYWHEEL_PROVENANCE=rust-agentforge-runner
    touch "/home/eveselove/agentforge/.pure_rust_flywheel" 2>/dev/null || true
fi
SNIP_EOF
chmod 644 "$SNIPPET" 2>/dev/null || true

if ! _is_sourced; then
  if _is_rust_disabled; then
    echo "=== AgentForge Rust Flywheel - DISABLED (safe rollback) ==="
    echo "  AGENTFORGE_RUST_FLYWHEEL=0 (via DISABLE_RUST_FLYWHEEL or $DISABLE_FILE)"
  else
    echo "=== AgentForge Rust Flywheel - PRODUCTION ENABLE (DEFAULT for Antigravity) ==="
    echo "  AGENTFORGE_RUST_FLYWHEEL=1"
    echo "  AGENTFORGE_USE_RUST=1"
    echo "  Rust runner: $AGENTFORGE_RUST_RUNNER"
    echo "  Snippet for sourcing: $SNIPPET"
    echo "  Rollback: DISABLE_RUST_FLYWHEEL=1 or touch $DISABLE_FILE"
  fi
fi

# Verify binary exists (warning only)
if [[ ! -x "$AGENTFORGE_RUST_RUNNER" ]]; then
    if ! _is_sourced; then
      echo "WARNING: Rust binary not found at $AGENTFORGE_RUST_RUNNER"
      echo "         Build it first: cd $AGENTFORGE_DIR/rust && cargo build -p agentforge-runner --release"
    fi
fi

# Make sure post_process and phase2_3_integration see the flags (they already check env + disable file)
if ! _is_sourced; then
  if ! _is_rust_disabled; then
    echo "post_process.py and phase2_3_integration.py will auto-trigger (rate-limited) Rust flywheel on real Antigravity tasks (DEFAULT ON)."
  else
    echo "Rust flywheel disabled for post_process / phase2_3_integration (explicit rollback active)."
  fi
fi

# NEW: Also invoke the canonical tiny Python activator (agentforge/enable_rust_flywheel.py)
# This guarantees the idempotent post_process patch + robust binary check + env.
# Safe + non-fatal if python not available. Skip entirely on disable.
if ! _is_rust_disabled && command -v python3 >/dev/null 2>&1; then
  if ! _is_sourced; then
    echo "[enable] Invoking Python module for patch + activation..."
  fi
  PYTHONPATH="$ROOT" python3 -c '
import sys, os
sys.path.insert(0, "/home/eveselove")
try:
    import agentforge.enable_rust_flywheel as erf
    erf.activate(quiet='"$(_is_sourced && echo "True" || echo "False")"', force=False, patch_post_process=True)
except Exception as _e:
    if not '"$(_is_sourced && echo "True" || echo "False")"'; then
        echo "[enable] Python activator warning (non-fatal, shell envs active): $_e" >&2
    fi
' 2>/dev/null || true
fi

# Optional immediate actions (only when run directly)
if ! _is_sourced; then
  if [[ "${1:-}" == "--run-now" ]]; then
      PYTHONPATH="$ROOT" python -m agentforge.rust_flywheel_step --real-data --use-rust --limit 40
  fi

  if [[ "${1:-}" == "--test-post-process" && -n "${2:-}" ]]; then
      PYTHONPATH="$ROOT" python -c "
import os
os.environ['AGENTFORGE_RUST_FLYWHEEL'] = '1'
from agentforge.eval.post_process import post_process_task
print(post_process_task('$2'))
"
  fi

  echo ""
  echo "To make permanent for the running farm (workers + services):"
  echo "  1. source $SNIPPET   (in grok_worker.sh, jules_worker.sh, dispatcher.sh, runners early)"
  echo "  2. Or run this script then restart systemd units (see JULES_LIVE_WORKER_INTEGRATION.md)"
  echo "  3. Rebuild release binary: cd $AGENTFORGE_DIR/rust && cargo build -p agentforge-runner --release"
  echo ""
  echo "Next real task completion (post_process) will feed the Rust self-improvement flywheel (rate limited, DEFAULT)."
  echo "Dedicated after-task hook (now default-on unless disabled, 5min rate-limit): bin/rust_flywheel_after_task.sh"
  echo "  (invoked from updated grok/jules_worker + dispatcher; rollback via DISABLE or .disable_rust_flywheel)"
  echo ""
  echo "🚀 2026-06: Rust Flywheel is now DEFAULT for Antigravity. Full package in ANTIGRAVITY_DEFAULT.md (story + blurb)."
  echo "   Clean symmetric disable:  bash bin/disable_rust_flywheel.sh"
fi

# === PURE RUST FLYWHEEL DEFAULT (injected by make_pure_rust_flywheel_default.sh @ 2026-05-31T10:42:02+03:00) ===
# Pure Rust cutover (production excellence): when .pure_rust_flywheel or AGENTFORGE_PURE_RUST_FLYWHEEL=1 or FLYWHEEL_ENGINE=rust,
# force sole use of agentforge-runner binary for ALL flywheel/candidate/continuous orchestration.
# Complements env snippet + unit patches. Idempotent + guarded. Ultimate killswitch: DISABLE_RUST_FLYWHEEL=1.
PURE_MARKER="/home/eveselove/agentforge/.pure_rust_flywheel"
if [[ -f "$PURE_MARKER" ]] || [[ "${AGENTFORGE_PURE_RUST_FLYWHEEL:-0}" = "1" ]] || [[ "${AGENTFORGE_FLYWHEEL_ENGINE:-}" = "rust" ]]; then
    export AGENTFORGE_PURE_RUST_FLYWHEEL=1
    export AGENTFORGE_FLYWHEEL_ENGINE=rust
    if [ -x "/home/eveselove/agentforge/rust/target/release/agentforge-runner" ]; then
        export AGENTFORGE_RUST_RUNNER="/home/eveselove/agentforge/rust/target/release/agentforge-runner"
    fi
    export AGENTFORGE_FLYWHEEL_PROVENANCE="rust-agentforge-runner"
    # shellcheck disable=SC1091
    [ -f "/home/eveselove/agentforge/bin/rust_flywheel.env" ] && source "/home/eveselove/agentforge/bin/rust_flywheel.env" 2>/dev/null || true
fi
# End pure section — DISABLE_RUST_FLYWHEEL remains ultimate global off-switch everywhere.
