LOG_FILE="$HOME/planlytasksko/guardian.log"
MAX_TMUX_WINDOWS=20          # ???????????????? ???????? ?? ???????????? agents
MAX_SWAP_PERCENT=60           # ?????????? ?????????? ?????? ???????????? (%)
MAX_USERS=30                  # ???????????????? SSH-????????????
TMUX_SESSION="agents"         # ?????? ???????????? ??????????????

log() {
    echo "[$(date '+%Y-%m-%d %H:%M:%S')] $1" >> "$LOG_FILE"
}

# --- 1. ?????????????? ???????????????? tmux-???????? ?????? ???????????????? ?????????????????? ---
cleanup_tmux() {
    if ! tmux has-session -t "$TMUX_SESSION" 2>/dev/null; then
        return
    fi

    local window_count
    window_count=$(tmux list-windows -t "$TMUX_SESSION" 2>/dev/null | wc -l)

    if [ "$window_count" -gt "$MAX_TMUX_WINDOWS" ]; then
        log "WARN: ???????????? '$TMUX_SESSION' ?????????? $window_count ???????? (??????????: $MAX_TMUX_WINDOWS)"

        # ?????????????? ???????? ?????? ???????????????? ???????????????? ?????????????????? (???????????? idle bash/zsh)
        local killed=0
        for idx in $(tmux list-windows -t "$TMUX_SESSION" -F '#{window_index}' | sort -rn); do
            # ???? ?????????????? ???????????? ????????
            [ "$idx" -eq 0 ] && continue

            local pane_pid
            pane_pid=$(tmux display-message -t "$TMUX_SESSION:$idx" -p '#{pane_pid}' 2>/dev/null)
            [ -z "$pane_pid" ] && continue

            # ?????????????????? ???????? ???? ???????????????? ???????????????? (?????????? ???????????? shell)
            local children
            children=$(pgrep -P "$pane_pid" 2>/dev/null | wc -l)

            if [ "$children" -eq 0 ]; then
                tmux kill-window -t "$TMUX_SESSION:$idx" 2>/dev/null
                killed=$((killed + 1))
            fi
        done

        if [ "$killed" -gt 0 ]; then
            log "CLEANUP: ?????????? $killed ???????????? tmux-???????? ?? '$TMUX_SESSION'"
        fi
    fi
}

# --- 2. ???????????????????? ???????????? ?? ?????????? ---
check_memory() {
    local swap_total swap_used swap_percent
    swap_total=$(free | awk '/????????????????:|Swap:/ {print $2}')
    swap_used=$(free | awk '/????????????????:|Swap:/ {print $3}')

    if [ "$swap_total" -gt 0 ] 2>/dev/null; then
        swap_percent=$((swap_used * 100 / swap_total))
        if [ "$swap_percent" -gt "$MAX_SWAP_PERCENT" ]; then
            log "ALERT: Swap ?????????????????????? ???? ${swap_percent}% ($((swap_used / 1024))Mi / $((swap_total / 1024))Mi)"

            # ??????-5 ?????????????????? ???? ????????????
            log "TOP MEM: $(ps aux --sort=-%mem | head -6 | tail -5 | awk '{printf "%s(%s%%) ", $11, $4}')"
        fi
    fi
}

# --- 3. ???????????????? SSH-???????????? ---
check_ssh_sessions() {
    local user_count
    user_count=$(who | wc -l)

    if [ "$user_count" -gt "$MAX_USERS" ]; then
        log "ALERT: $user_count SSH-???????????? (??????????: $MAX_USERS)"
    fi
}

# --- 4. ???????????????? zombie-?????????????????? ---
cleanup_zombies() {
    local zombie_count
    zombie_count=$(ps aux | awk '$8=="Z"' | wc -l)

    if [ "$zombie_count" -gt 0 ]; then
        log "WARN: ?????????????? $zombie_count zombie-??????????????????"
        # ???????????????????? SIGCHLD ?????????????????? ??????????
        ps aux | awk '$8=="Z" {print $2}' | while read -r zpid; do
            local ppid
            ppid=$(ps -o ppid= -p "$zpid" 2>/dev/null | tr -d ' ')
            if [ -n "$ppid" ] && [ "$ppid" -gt 1 ]; then
                kill -s SIGCHLD "$ppid" 2>/dev/null
            fi
        done
    fi
}

# --- 5. ?????????????? ?????????? (???????? 1MB) ---
rotate_log() {
    if [ -f "$LOG_FILE" ]; then
        local size
        size=$(stat -c%s "$LOG_FILE" 2>/dev/null || echo 0)
        if [ "$size" -gt 1048576 ]; then
            mv "$LOG_FILE" "${LOG_FILE}.old"
            log "LOG: ?????????????? ???????? (?????? ${size} ????????)"
        fi
    fi
}

# === ???????????? ???????? ???????????????? ===
rotate_log
cleanup_tmux
check_memory
check_ssh_sessions
cleanup_zombies
